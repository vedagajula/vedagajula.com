        <h2 class="title">Science behind the addiction to short content</h2>
        <div align="center"><img src='https://rishavkumar.io/veda/assets/tiktok3.webp'></div><br><br>
        <p>
            Addiction to these kind of short-video format applications are <a href="https://sitn.hms.harvard.edu/flash/2018/dopamine-smartphones-battle-time/"> due to the dopamine release</a> that happens in our brain once we scroll through these videos. It has been found that, addiction to this kind of social media releases 
            the same amount of dopamine that would have been released if a person was smoking. This is something serious to consider and improve our lives from. It essentially means that it is no less an addiction than being addicted to smoking and that is something to be worried about and bring 
            about a change.
        </p>
