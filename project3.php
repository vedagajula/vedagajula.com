        <h2 class="title">Addiction to Short Video Content Format Applications & their effect on today's youth</h2>
        <div align="center"><img src='https://rishavkumar.io/veda/assets/tiktok1.jpeg'></div><br><br>
        <p>
            Short video format applications are essentially applications that live on someone's mobile phone that gives access to millions of videos that are short in length, usually 60 seconds. These live on your mobile phones, that makes you prone to launch these apps whenever you feel bored.
            Many people have raised awareness about this issue of <a href="https://fixingport.com/why-is-tiktok-so-addictive/">being addicted to TikTok and similar short video format applications</a> that are creating a huge distraction.
        </p>
        <br>
        <p>
            These applications have become the reason for today's youth to become addicted to these types of content which is making them more and more addicted and making their attention span shorter. This has been tested and 
            has been proven that these types of applications where people are able to consume contents that consists of jump cuts and rapid motion within a short span releases dopamine
            in their brains and causes a lot of stress release that usually is caused by smoking. Experts recommend a process called "Dopamine Detox" to get rid of these problems. These kinds of 
            content have been proven to decrease the attention span of many people thus hindering their studies to a great extent.
        </p>
        <br>
